#include	<stdio.h>

void	ft_swap(int a, int b)
{
	int	tmp;

	tmp = a;
	a = b;
	b = tmp;
}

int	main(void)
{
	int p = 2;
	int q = 9;
	ft_swap(p, q);
	printf("%d\n", p);
	printf("%d\n", q);
}
