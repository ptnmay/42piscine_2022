int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb == 1)
		return (1);
	while (nb > i && i * i < nb && i <= 46340)
	{
		i++;
		if (i * i == nb)
			return (i);
	}
	return (0);
}

#include	<stdio.h>
int	main(void)
{
	printf("%d\n", ft_sqrt(2147395600));
}
