#include <unistd.h>

void	ft_putchar(char n)
{
	write(1, &n, 1);
}

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		ft_putchar(*str);
		str++;
	}
}


#include <stdio.h>
#include <string.h>

int	main(void)
{
	char str[] = "Hola en worldes!";
	ft_putstr(str);
}
