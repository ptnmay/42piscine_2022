#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char	a[] = "meaw";
	char	b[20];
	
	ft_strcpy(b, a);
	printf("%s\n%s",a ,b);
	return (0);
}
